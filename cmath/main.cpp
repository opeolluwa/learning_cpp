#include <iostream>
#include <cmath>

using std::cout;
using std::endl;

int main()
{
    cout << "the absolute value of -34.67  is " << std::abs(-34.67) << endl;
    cout << "the cosine value of -34.67  is " << std::cos(-34.67) << endl;
    cout << "the sin value of -34.67  is " << std::sin(-34.67) << endl;
    cout << "the tangent value of -34.67  is " << std::tan(-34.67) << endl;
    cout << "the whole number value of -34.67  is " << std::ceil(-34.67) << endl;
    cout << "the absolute rounded value of -34.67  is " << std::round(-34.67) << endl;
}